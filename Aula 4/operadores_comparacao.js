const estaAprovado = true;

if (estaAprovado === true) {
    console.log('Aprovado');
}

// Compara o conteúdo do valor ==
if ('0' == 0) {
    console.log("'0' = 0");
} else {
    console.log("'0' != 0");
}

// Compara o valor e o tipo de dado ===
if ('0' === 0) {
    console.log("'0' = 0");
} else {
    console.log("'0' != 0");
}

// Preferência pela utilização do ===